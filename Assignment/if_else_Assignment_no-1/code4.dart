/*

Write a dart program, take a number and print whether it is
positive or negative.
Input: var=5
Output: 5 is a positive number
Input: var=-9
Output: -9 is a negative number

 */

 void main(){

    var x=5;

    if(x>0){

        print("${x} is a positive number");
    }else{

        print("${x} is a negative number");
    }

 }